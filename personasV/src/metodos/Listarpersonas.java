    private void ModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModificarActionPerformed
    int count =  ((DefaultTableModel)jTabla.getModel()).getRowCount();
        frmAgregar.pers = new persona[count];
        for(int i = 0; i<count; i++){
            persona perTmp = new persona();
            perTmp.tipodoc = "" + ((DefaultTableModel)jTabla.getModel()).getValueAt(i, 0);
            perTmp.ndoc = "" + ((DefaultTableModel)jTabla.getModel()).getValueAt(i, 1);
            perTmp.noms = "" + ((DefaultTableModel)jTabla.getModel()).getValueAt(i, 2);
            perTmp.apells = "" + ((DefaultTableModel)jTabla.getModel()).getValueAt(i, 3);
            

            frmAgregar.pers[i] = perTmp;
        }
        setVisible(false);
    }//GEN-LAST:event_ModificarActionPerformed

