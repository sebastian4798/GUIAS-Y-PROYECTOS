    private void ajustarDatos(persona[] personas){
        pers = new Object[personas.length][4];
        for(int i = 0; i < personas.length;i++){
            System.out.println("pers: " + personas[i]);
            pers[i][0] = personas[i].tipodoc;
            pers[i][1] = personas[i].ndoc;
            pers[i][2] = personas[i].noms;
            pers[i][3] = personas[i].apells;
            
        }
    }
   
